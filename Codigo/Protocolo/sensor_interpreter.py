def interpret(identifier):
	if identifier == '1':
		return "Movimiento"
	elif identifier == '2':
		return "Sonido (Big Sound)"
	elif identifier == '3':
		return "Luz"
	elif identifier == '4':
		return "Shock"
	elif identifier == '5':
		return "Touch"
	elif identifier == '6':
		return "Humedad"
	elif identifier == '7':
		return "Big Sound"
	elif identifier == '8':
		return "Temperatura"
	elif identifier == '9':
		return "Ultrasonico"
		
	else:
		return "Generico"
